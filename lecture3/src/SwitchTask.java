
public class SwitchTask {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int number = 2;
		
		switch(number){
		case 1:
			System.out.println("ONE");
		break;
		case 2:
			System.out.println("TWO");
			break;
			default:
				System.out.println("Not number represent");
		}
		
	}

}
